; DATA SEGMENT
DATA SEGMENT
    M1 DB 10,13,"ENTER NUMBER: $"
    M2 DB 10,13,"ODD$"
    M3 DB 10,13,"EVEN$"
DATA ENDS

; MACRO TO PRINT A MESSAGE
PRTMSG MACRO MESSAGE
    LEA DX, MESSAGE
    MOV AH, 09H
    INT 21H
ENDM

; MACRO TO GET A DECIMAL DIGIT
GETDCM MACRO
    MOV AH, 01H
    INT 21H
    SUB AL, 30H
ENDM

; CODE SEGMENT
CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    PRTMSG M1
    GETDCM
    GETDCM ; for two digit numbers

    SHR AL, 01  
    JC ODD      

    PRTMSG M3   
    JMP DONE

ODD:
    PRTMSG M2   

DONE:
    MOV AH, 4CH
    INT 21H
CODE ENDS

END START
